// Write a program in C to print 'Hello' 5 times using for loop.

#include<stdio.h>
int main()
{
    int i=1;
    for(i=1;i<=5;i++)   // In case of for loop the initialization, condition, increment all are performed in a single line respectively.
    {
        printf("Hello\n");
    }
    return 0;
}


/*
Use of loop is execute some task multiple times.
*/
